use std::sync::atomic::{AtomicU8, Ordering};

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Locale {
    ZhCn,
    En,
}

impl Locale {
    pub fn from_code(code: &str) -> Option<Self> {
        match code {
            "en" => Some(Self::En),
            "zh-CN" | "zh" => Some(Self::ZhCn),
            _ => None,
        }
    }

    pub fn to_code(self) -> &'static str {
        match self {
            Self::En => "en",
            Self::ZhCn => "zh-CN",
        }
    }

    pub fn display_name(self) -> &'static str {
        match self {
            Self::En => "English",
            Self::ZhCn => "中文",
        }
    }

    /// 检测系统语言：非中文环境 → English，否则 → 中文
    pub fn detect_system() -> Self {
        let lang = sys_locale::get_locale().unwrap_or_default();
        if lang.starts_with("zh") {
            Self::ZhCn
        } else {
            Self::En
        }
    }
}

static CURRENT_LOCALE: AtomicU8 = AtomicU8::new(0);

pub fn set_locale(locale: Locale) {
    CURRENT_LOCALE.store(locale as u8, Ordering::Release);
}

pub fn get_locale() -> Locale {
    match CURRENT_LOCALE.load(Ordering::Acquire) {
        1 => Locale::En,
        _ => Locale::ZhCn,
    }
}

#[macro_export]
macro_rules! tr {
    ($key:literal) => {
        $crate::translate($key)
    };
    ($key:literal, $($arg:expr),+ $(,)?) => {
        $crate::translate_fmt($key, &[$($arg.to_string()),+])
    };
}

pub fn translate(key: &'static str) -> &'static str {
    match get_locale() {
        Locale::En => en(key),
        Locale::ZhCn => key,
    }
}

pub fn translate_fmt(key: &'static str, args: &[String]) -> String {
    let template = translate(key);
    let mut result = String::with_capacity(template.len() + args.len() * 8);
    let mut arg_iter = args.iter();
    let mut last_end = 0;
    let bytes = template.as_bytes();
    let mut i = 0;
    while i < bytes.len() {
        if i + 1 < bytes.len() && bytes[i] == b'{' && bytes[i + 1] == b'}' {
            result.push_str(&template[last_end..i]);
            if let Some(arg) = arg_iter.next() {
                result.push_str(arg);
            }
            i += 2;
            last_end = i;
        } else {
            i += 1;
        }
    }
    result.push_str(&template[last_end..]);
    result
}

